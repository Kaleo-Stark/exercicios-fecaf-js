// Operador && (E)
true  && true  // ... true
false && false // ... false
true  && false // ... false
false && true  // ... false

// Operador || (OU)
true  || true  // ... true
false || false // ... false
true  || false // ... true
false || true  // ... treu

// Operador ! (NOT)
!true  // ........... false
!false // ........... true

// Operador ^ (XOR)
true  ^ true  // .... 0
false ^ false // .... 0
true  ^ false // .... 1
false ^ true  // .... 1